/**
 * generate-certificates.cjs
 *
 * Scans public/certificates/ for PDF files and generates
 * src/data/certificatesGenerated.js with a 1:1 mapping:
 *   title  = filename without extension
 *   link   = BASE_URL + 'certificates/<filename>'
 *
 * Run: node scripts/generate-certificates.cjs
 * This is also wired as a "prebuild" / "predev" script in package.json.
 */

const fs = require('fs');
const path = require('path');

const CERT_DIR = path.join(__dirname, '..', 'public', 'certificates');
const OUTPUT = path.join(__dirname, '..', 'src', 'data', 'certificatesGenerated.js');

// Read all PDF files from the certificates directory
const files = fs.readdirSync(CERT_DIR)
  .filter(f => /\.pdf$/i.test(f))
  .sort((a, b) => a.localeCompare(b));

if (files.length === 0) {
  console.warn('⚠  No PDF files found in public/certificates/');
}

// Build the JS source
const entries = files.map((filename, index) => {
  const title = filename.replace(/\.pdf$/i, '');
  // Encode for URL. encodeURIComponent properly encodes + to %2B, which is required for static file serving.
  const encodedFilename = encodeURIComponent(filename);
  return `  {
    id: ${index + 1},
    title: ${JSON.stringify(title)},
    file: ${JSON.stringify(filename)},
    link: import.meta.env.BASE_URL + ${JSON.stringify('certificates/' + encodedFilename)},
  }`;
});

const output = `// AUTO-GENERATED — do not edit manually.
// Generated by scripts/generate-certificates.cjs from public/certificates/
// Re-run: node scripts/generate-certificates.cjs

export const certificatesList = [
${entries.join(',\n')}
];
`;

fs.writeFileSync(OUTPUT, output, 'utf8');
console.log(`✅ Generated ${files.length} certificate(s) → ${path.relative(path.join(__dirname, '..'), OUTPUT)}`);
files.forEach(f => console.log(`   • ${f}`));
